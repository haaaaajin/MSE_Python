for i in nums :      # 리스트 nums 내의 원소를 모두 사용할 때까지 4번줄의 print()함수를 실행해 '-------'를 출력한다.
    print("-------") # 리스트 nums의 원소가 i에 들어가 for문이 총 4회 반복되지만, 변수 i는 따로 실행되는 명령어가 없으므로 리스트 안의 원소는 어떤 것이 들어가도 상관 없다.