icecream = {'폴라포': 1200, '빵빠레': 1800, '월드콘': 1500, '메로나': 1000}
# 폴라포, 빵빠레, 월드콘, 메로나 를 key로 가지고, 1200, 1800, 1500, 1000을 value로 가지는 딕셔너리 icecream을 만듦
icecream['누가바'] 
# 딕셔너리 icecream에 '누가바'를 key로 가지는 원소가 있다면 value 값을 알 수 있었을 것이다.
# 하지만 위 딕셔너리에는 '누가바'를 key로 가지는 원소가 없기 때문에, 위 코드에서 에러가 발생했다.
# 만약 3번 줄에서 print(icecream['폴라포']) 라는 코드를 작성했으면, '폴라포'의 value값인 1200가 결과값으로 나왔을 것이다.
